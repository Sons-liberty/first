import tkinter as tk
import random
import threading


def dow():
      window = tk.Tk()
      width = window.winfo_screenwidth()
      height = window.winfo_screenheight()
      a = random.randrange(0, width)
      b = random.randrange(0, height)
      window.title('201717')
      window.geometry("220x50" + "+" + str(a) + "+" + str(b))

      # 提示文字列表（已添加新内容）
      tips = [
            '天冷加衣', '对自己好点', '不要熬夜', '好好吃饭', '天天开心', '我想你了'
      ]
      tip = random.choice(tips)

      # 多样的颜色背景
      bg_colors = [
            'pink', 'blue', 'orange', 'purple', 'gray'
      ]
      bg = random.choice(bg_colors)

      tk.Label(
            window,
            text=tip,
            bg=bg,
            font=('行楷', 18),
            width=30,
            height=4
      ).pack()

      # 进入窗口主循环
      window.mainloop()


